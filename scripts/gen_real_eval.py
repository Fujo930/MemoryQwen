for fname, questions in all_combined.items():
    content = f"# Real Eval Questions — {Path(fname).stem}\n\n"
    for j, (q, a, sources, guard, fail, trap) in enumerate(questions):
        content += f"## Q{j+1:03d}\n"
        content += f"topic: {Path(fname).stem.split('_')[0]}\n"
        content += f"question: {q}\n"
        content += f"expected_answer: {a}\n"
        content += f"expected_sources: {sources}\n"
        content += f"guard_expected: {'true' if guard else 'false'}\n"
        content += f"failure_type_if_wrong: {fail}\n"
        content += f"trap_level: {trap}\n\n"
    (D / fname).write_text(content, encoding="utf-8")
    total += len(questions)
    print(f"  {fname}: {len(questions)} questions")

